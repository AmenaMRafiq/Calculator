package model;

/**
 * This enumeration class consists of seven constants: LEFTBRACKET,
 * RIGHTBRACKET, TIMES, DIVIDE, PLUS, MINUS and INVALID. Used to check 
 * the Symbol of the Entry objects.
 * 
 * @author Amena Rafiq
 * @see    Entry
 */

public enum Symbol {    
  LEFTBRACKET("("),
  RIGHTBRACKET(")"),
  TIMES("*"),
  DIVIDE("/"),
  PLUS("+"),
  MINUS("-"),
  INVALID("Invalid");

  private String symbol;
  
  private Symbol(String symbol) {
    this.symbol = symbol;
  }

  /**
   * Returns the string symbol corresponding to each Symbol constant.
   * 
   * @return symbol 
   */
  public String toString() {
    return symbol;
  }
}


